void sprites_init(void) {
    /* Player: tiles 0-3 (frame 0), tiles 4-7 (frame 1, in 1D: +4, but for 16x16 it's base+0,base+1,base+16,base+17) */
    /* Actually for 1D 4bpp 16x16: tiles are base, base+1, base+16, base+17 */
    sprite_16x16(0, player_f0);
    sprite_16x16(32, player_f1); /* Next 16x16 sprite starts at tile 32 (16 tiles apart for 16x16) */
    
    /* Slime frame 0: tile 64 */
    sprite_16x16(64, slime_f0);
    /* Slime frame 1: tile 96 */
    sprite_16x16(96, slime_f1);
    
    /* Bat frame 0: tile 128 */
    sprite_16x16(128, bat_f0);
    /* Bat frame 1: tile 160 */
    sprite_16x16(160, bat_f1);
    
    /* Crack overlays: tiles 192, 193, 194 (8x8 each, 1 tile per stage) */
    for (int i = 0; i < 3; i++) {
        obj_tile_pattern(192 + i * 16, crack_tiles[i]); /* use 16-tile stride for 1D */
    }
    
    /* Particle tile at 512 (used by particle_system.c) */
}

