/* OBJ VRAM at 0x06010000, 1D mapping: tile N at offset N*32 bytes */

static void obj_tile_solid(u16 tile_id, u8 color_idx) {
    u16* addr = (u16*)(0x06010000 + tile_id * 32);
    u16 val = color_idx | (color_idx << 4) | (color_idx << 8) | (color_idx << 12);
    for (int i = 0; i < 16; i++) addr[i] = val;
}

static void obj_tile_pattern(u16 tile_id, const u8 pat[16]) {
    u16* addr = (u16*)(0x06010000 + tile_id * 32);
    for (int row = 0; row < 8; row++) {
        u8 a = pat[row * 2 + 0];
        u8 b = pat[row * 2 + 1];
        u16 lo = (a & 0xF) | ((a >> 4) << 4) | ((b & 0xF) << 8) | ((b >> 4) << 12);
        addr[row * 2 + 0] = lo;
        addr[row * 2 + 1] = lo;
    }
}

/* 16x16 sprite = 4 tiles (2x2 grid) in 1D mapping */
static void sprite_16x16(u16 base_tile, const u8 pat[4][16]) {
    obj_tile_pattern(base_tile + 0, pat[0]);     /* top-left */
    obj_tile_pattern(base_tile + 1, pat[1]);     /* top-right */
    obj_tile_pattern(base_tile + 16, pat[2]);    /* bottom-left */
    obj_tile_pattern(base_tile + 17, pat[3]);    /* bottom-right */
}

